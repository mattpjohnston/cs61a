def distance(city_a, city_b):
    """
    Returns the distance between city_a and city_b according to their
    coordinates.

    >>> city_a = make_city('city_a', 0, 1)
    >>> city_b = make_city('city_b', 0, 2)
    >>> distance(city_a, city_b)
    1.0
    >>> city_c = make_city('city_c', 6.5, 12)
    >>> city_d = make_city('city_d', 2.5, 15)
    >>> distance(city_c, city_d)
    5.0
    """
    # The distance between two coordinate pairs (x1, y1) and (x2, y2) can be found by
    # calculating the sqrt of (x1 - x2)**2 + (y1 - y2)**2
    x1, y1 = get_lat(city_a), get_lon(city_a)
    x2, y2 = get_lat(city_b), get_lon(city_b)
    return sqrt(((x1 - x2) ** 2) + ((y1 - y2) ** 2))


